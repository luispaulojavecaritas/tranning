private void cargarDatos(){
        SexoDao sexoDao = new SexoDaoImpl();
        List<Sexo> listaSexo = new ArrayList<>();
        listaSexo = sexoDao.findAll();
        
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("Id");
        dtm.addColumn("Descripci√≥n");
        dtm.addColumn("Estado");
        
        String[] arreglo = new String[4];
        
        for(int i = 0; i<= listaSexo.size()-1; i++){            
            arreglo[0] = listaSexo.get(i).getId().toString();
            arreglo[1] = listaSexo.get(i).getDescripcion();
            arreglo[2] = listaSexo.get(i).getEstado()+"";
         
            dtm.addRow(arreglo);
        }
        
        tblSexo.setModel(dtm);        
    }


